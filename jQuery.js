$(function () {
  var img = $('img')
  img.animate({ opacity: 1 })
  img.animate({ height: '+=300px', width: '+=300px', top: '+=300px' }, 500)
  img.animate({ height: '-=300px', width: '-=300px', left: '+=300px' }, 500)
  img.animate({ height: '+=300px', width: '+=300px', top: '-=300px' }, 500)
  img.animate({ height: '-=300px', width: '-=300px', left: '-=300px' }, 500)
  img.animate({ opacity: 0.1 })
  img.animate({ opacity: 1.0 }, 1000)
  img.animate({ opacity: 0.1 }, 1000)
  img.animate({ opacity: 1.0 }, 1000)
  img.animate({ opacity: 0.1 }, 1000)
  img.animate({ opacity: 1.0 }, 1000)
  img.animate({ opacity: 0.1 }, 1000)
  img.animate({ opacity: 1.0 }, 1000)
})
