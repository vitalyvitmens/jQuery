$(function () {
  var div = $('img')
  div.animate({ opacity: 1 })
  div.animate({ height: '+=300px', width: '+=300px', top: '+=300px' }, 500)
  div.animate({ height: '-=300px', width: '-=300px', left: '+=300px' }, 500)
  div.animate({ height: '+=300px', width: '+=300px', top: '-=300px' }, 500)
  div.animate({ height: '-=300px', width: '-=300px', left: '-=300px' }, 500)
  div.animate({ opacity: 0.1 })
  div.animate({ opacity: 1.0 }, 1000)
  div.animate({ opacity: 0.1 }, 1000)
  div.animate({ opacity: 1.0 }, 1000)
  div.animate({ opacity: 0.1 }, 1000)
  div.animate({ opacity: 1.0 }, 1000)
  div.animate({ opacity: 0.1 }, 1000)
  div.animate({ opacity: 1.0 }, 1000)
})
